package br.ada.caixa.JavaCompleto.POO.Exercicio;

public class Classe1 {
    int b;
    int B;

    public int calcularArea() {
        return b * B;
    }

    public int calcularPerimetro() {
        return 2 * b + 2 * B;
    }

    public int calcularHipotenusa(){
        return (b*b + B*B);
    }

}
